    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        // TODO add your handling code here:
        String kode = txtkode.getText();        
        String judul = txtjudul.getText();
        String nama = txtpenulis.getText();
        String tahun = txttahunterbit.getText();
        
        if (path2 == null) {
        JOptionPane.showMessageDialog(null, "Harus ada foto", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Exit the method without attempting to save
    }
        
        try{
            Connection c = Koneksi.getKoneksi();
            String sql = "INSERT INTO buku(id_buku, judul_buku, nama_penulis, tahun_terbit, imageData) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, kode);
            p.setString(2, judul);
            p.setString(3, nama);
            p.setString(4, tahun);
            InputStream is = new FileInputStream(new File(path2));
            p.setBlob(5, is);
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
        }catch(SQLException e){
            e.printStackTrace(); 
            
            System.out.println("Terjadi Kesalahan");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(menuutama.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            loadData();
            autonumber();

        }
        
        
    }//GEN-LAST:event_btnsubmitActionPerformed

