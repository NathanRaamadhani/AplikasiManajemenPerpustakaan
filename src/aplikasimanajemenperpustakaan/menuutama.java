    private void loadData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try{
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            
            String sql = "SELECT * FROM buku";
            ResultSet r = s.executeQuery(sql);
            
            while(r.next()){
                Object[] o = new Object[5];
                o [0] = r.getString("id_buku");
                o [1] = r.getString("judul_buku");
                o [2] = r.getString("nama_penulis");
                o [3] = r.getString("tahun_terbit");
                Blob blob = (Blob) r.getBlob("imageData");
                if (blob != null) {
                byte[] imageBytes = blob.getBytes(1, (int) blob.length());
                ImageIcon imageIcon = new ImageIcon(imageBytes);
                Image scaledImage = imageIcon.getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH);
                o[4] = new ImageIcon(scaledImage);
            } else {
                o[4] = null;
            }      
                              
                
                model.addRow(o);
            }
            r.close();
            s.close();
        }catch(SQLException e){
            System.out.println("terjadi kesalahan");
        }    }

    private void autonumber() {
        try{
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM buku ORDER BY id_buku DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()){
                String idBuku = r.getString("id_buku").substring(2);
                String TH = "" + (Integer.parseInt(idBuku) + 1);
                String Nol = "";
                
                if(TH.length()==1){
                    Nol = "00";
                }else if(TH.length()==2){
                    Nol = "0";
                }else if(TH.length()==3){
                    Nol = "";
                }
                
                txtkode.setText("BK" + Nol + TH);
                
            }else{
                txtkode.setText("BK001");
            }
            r.close();
            s.close();
        }catch(Exception e){
                    e.printStackTrace();

            System.out.println("autonumber error");
        }    }
    
    class ImageRenderer extends DefaultTableCellRenderer {
    @Override
    public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
        JLabel label = (JLabel) super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
        if (value instanceof ImageIcon) {
            label.setIcon((ImageIcon) value);
            label.setText("");
        }
        return label;
        }
    }
    
    public class MultiLineTableCellRenderer extends DefaultTableCellRenderer {
    private JTextArea textArea;

    public MultiLineTableCellRenderer() {
        this.textArea = new JTextArea();
        this.textArea.setLineWrap(true);
        this.textArea.setWrapStyleWord(true);
    }

    @Override
    public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
        textArea.setText((value == null) ? "" : value.toString());
        return textArea;
    }
}
