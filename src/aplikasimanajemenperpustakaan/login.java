    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String nama = textNama.getText();
        String password = textPassword.getText(); // Perlu diganti jika text_nama adalah teks password.

        // Lakukan autentikasi pengguna di sini, misalnya dengan memeriksa ke database.
        // Jika autentikasi berhasil, arahkan ke MenuUtama.
        boolean autentikasiBerhasil = autentikasi(nama, password); // Fungsi autentikasi harus diimplementasikan.

        if (autentikasiBerhasil) {
            menuutama menuutama = new menuutama();
            menuutama.setVisible(true);
            this.dispose(); // Tutup frame login setelah masuk berhasil.
        } else {
            // Tampilkan pesan kesalahan atau notifikasi autentikasi gagal.
            JOptionPane.showMessageDialog(this, "Autentikasi gagal. Nama atau Password salah.");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

