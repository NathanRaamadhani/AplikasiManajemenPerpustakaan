    private boolean autentikasi(String nama, String password) {
        try {
            // Lakukan koneksi ke database
            Connection c = Koneksi.getKoneksi();

            // Buat statement untuk mengecek autentikasi
            String query = "SELECT * FROM petugas WHERE nama = ? AND password = ?";
            PreparedStatement preparedStatement = c.prepareStatement(query);
            preparedStatement.setString(1, nama);
            preparedStatement.setString(2, password);

            // Jalankan query
            ResultSet resultSet = preparedStatement.executeQuery();

            // Jika ada baris yang sesuai, autentikasi berhasil
            return resultSet.next();
        } catch (SQLException e) {
            e.printStackTrace(); // Atau Anda bisa menangani exception sesuai kebutuhan aplikasi Anda
            return false;
        }
    }

